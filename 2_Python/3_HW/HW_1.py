def choco_break_check(m: int, n: int, k: int) -> bool:
    return any([k % n == 0, k % m == 0])


print_separator_text("*", f"Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек,\n"
                          f"если разрешается сделать один разлом по прямой между дольками\n"
                          f"(то есть разломить шоколадку на два прямоугольника).")
m, n, k = map(int, input("Введите m, n, k разделяя их пробелом: ").split(' '))
print(f"yes") if choco_break_check(m, n, k) else print("no")
