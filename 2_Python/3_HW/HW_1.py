def digits_sum(number: int) -> (int, int):
    """функция ищет сумму цифр числа любого порядка"""
    number_1, number_2, sum_var_1, sum_var_2 = abs(number), abs(number), 0, 0
    # первый способ
    while number_1 > 0:
        sum_var_1 += number_1 % 10
        number_1 = number_1 // 10
    # второй способ
    sum_var_2 = sum([int(x) for x in str(number_2)])
    return sum_var_1, sum_var_2


print_separator_text("*", "Найдите сумму цифр трехзначного числа")

sum_var_1, sum_var_2 = digits_sum(int(input("Введите число: ")))
print(f"Сумма цифр числа первым способом: {sum_var_1}\n"
      f"Сумма цифр числа вторым способом: {sum_var_2}")


