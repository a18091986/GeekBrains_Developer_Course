def min_distance(arr: list, x: int) -> int:
    temp_dist = x - arr[0]
    min_dist_el = arr[0]
    for el in arr[1:]:
        if abs(x - el) <= temp_dist:
            min_dist_el = el
            temp_dist = x - el
    return min_dist_el


