cost_russian = {'а': 1, 'в': 1, 'е': 1, "и": 1, "н": 1, "о": 1, "р": 1, "с": 1, "т": 1, "д": 2, "к": 2, "л": 2, "м": 2,
                "п": 2, "у": 2, "б": 3, "г": 3, "ё": 3, "ь": 3, "я": 3, "й": 4, "ы": 4, "ж": 5, "з": 5, "х": 5, "ц": 5,
                "ч": 5, "ш": 8, "э": 8, "ю": 8, "ф": 10, "щ": 10, "ъ": 10}

cost_english = {'a': 1, 'e': 1, 'i': 1, "o": 1, "u": 1, "l": 1, "n": 1, "s": 1, "t": 1, "r": 2, "d": 2, "g": 2, "b": 2,
                "c": 2, "m": 2, "p": 3, "f": 3, "h": 3, "v": 3, "w": 3, "y": 4, "k": 5, "j": 8, "х": 8, "q": 5,
                "z": 5}

russian_letters = cost_russian.keys()
english_letters = cost_english.keys()


def scrabble_word_cost(word: str) -> int:
    sum_cost = 0
    word = word.lower()
    dictionary = cost_english
    if word[0] in cost_russian:
        dictionary = cost_russian
    for ch in word:
        sum_cost += dictionary.get(ch)
    return sum_cost


current_letters = random.choice([russian_letters, english_letters])
random_word = ''.join(random.sample(list(current_letters), random.randint(2, 10)))

print(f"Слово {random_word} по правилам скарабл оценивается в {scrabble_word_cost(random_word)}")
